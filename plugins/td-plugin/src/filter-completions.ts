/**
 * Completion entry kinds that are always kept regardless of name.
 * Member kinds come from dot-access on specific types (not global scope).
 * Local kinds are user-defined symbols in the current scope.
 * Keywords are TypeScript language keywords (if, else, string, number, etc.).
 */
const ALWAYS_ALLOWED_KINDS: ReadonlySet<string> = new Set([
    "keyword",          // TypeScript keywords (if, else, string, number, etc.)
    "property",         // memberVariableElement -- dot-access members
    "method",           // memberFunctionElement -- dot-access methods
    "getter",           // memberGetAccessorElement
    "setter",           // memberSetAccessorElement
    "constructor",      // constructorImplementationElement
    "call",             // callSignatureElement
    "index",            // indexSignatureElement
    "construct",        // constructSignatureElement
    "local var",        // localVariableElement -- user-defined local variable
    "local function",   // localFunctionElement -- user-defined local function
    "local class",      // localClassElement
    "parameter",        // parameterElement -- function parameter
    "label",            // label -- for goto/break targets
    "string",           // string literal completions
]);

