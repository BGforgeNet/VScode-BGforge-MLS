/** Minimal completion entry shape -- only the fields we inspect. */
export interface CompletionEntryLike {
    readonly name: string;
    readonly kind: string;
}

/**
 * Filter ES lib names from .td file completions.
 * Keeps entries whose kind is in ALWAYS_ALLOWED_KINDS or whose name
 * is not in ES_LIB_BLOCKLIST.
 * Returns a new array (immutable -- does not modify the input).
 */
export function filterEsLibCompletions<T extends CompletionEntryLike>(
    entries: readonly T[],
): T[] {
    return entries.filter(
        e => ALWAYS_ALLOWED_KINDS.has(e.kind) || !ES_LIB_BLOCKLIST.has(e.name)
    );
}

/**
 * Filter TD runtime names from non-.td file completions.
 * Returns a new array (immutable -- does not modify the input).
 */
export function filterTdRuntimeCompletions<T extends CompletionEntryLike>(
    entries: readonly T[],
    tdNames: ReadonlySet<string>,
): T[] {
    return entries.filter(e => !tdNames.has(e.name));
}
