/**
 * TypeScript Language Service Plugin for TD (.td) files.
 * - Injects td-runtime.d.ts so TD API functions are available without a tsconfig.
 * - Overrides compiler lib to exclude DOM types that clutter completions.
 * - Filters completions in .td files using a static blocklist of all names
 *   declared in TypeScript's ES2020 lib files. Keywords (string, number, etc.)
 *   and member/local symbols are kept by kind, not by name.
 * - Filters TD-specific completions out of non-.td files (e.g. .tbaf).
 *
 * Uses getScriptFileNames override on the LanguageServiceHost to ensure the
 * runtime is included in the project's program. The getExternalFiles approach
 * does not reliably work for inferred projects (standalone files without tsconfig).
 *
 * Loaded by tsserver via contributes.typescriptServerPlugins in package.json
 * (VSCode) or via tsconfig.json plugins array (other editors).
 */

import type ts from "typescript";
