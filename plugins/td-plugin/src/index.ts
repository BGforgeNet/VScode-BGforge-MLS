function isTdFile(fileName: string): boolean {
    return fileName.endsWith(TD_EXTENSION);
}

/**
 * Extract all declared identifier names from source text.
 * Matches both explicit (`declare function X`) and implicit (`interface X`,
 * `type X`) declarations. Anchored to line start to skip JSDoc comment text.
 */
function extractDeclaredNames(content: string): ReadonlySet<string> {
    const names = new Set<string>();
    const re = /^[ \t]*(?:declare\s+)?(?:function|interface|type|var|const|let|class|enum)\s+(\w+)/gm;
    let match;
    while ((match = re.exec(content)) !== null) {
        const name = match[1];
        if (name !== undefined) names.add(name);
    }
    return names;
}

function init(modules: { typescript: typeof ts }): ts.server.PluginModule {
    // Memoized at init() scope (shared across create() calls) because the runtime
    // path depends on __dirname (the plugin's install location), not on the project.
    // All projects using this plugin share the same runtime file.
    let runtimePath: string | undefined;
    let runtimeExists = false;
    let tdNames: ReadonlySet<string> | undefined;

    function getRuntimePath(): string {
        if (runtimePath === undefined) {
            // When installed as npm package: node_modules/@bgforge/td-plugin/out/index.js
            // td-runtime.d.ts is in node_modules/@bgforge/mls-server/out/td-runtime.d.ts
            // When bundled in VSIX: node_modules/bgforge-td-plugin/index.js
            // td-runtime.d.ts is at ../../server/out/td-runtime.d.ts
            // Try both locations.
            const vsixPath = path.resolve(__dirname, "../../server/out/td-runtime.d.ts");
            const npmPath = path.resolve(__dirname, "../../@bgforge/mls-server/out/td-runtime.d.ts");
            if (fs.existsSync(vsixPath)) {
                runtimePath = vsixPath;
                runtimeExists = true;
            } else if (fs.existsSync(npmPath)) {
                runtimePath = npmPath;
                runtimeExists = true;
            } else {
                runtimePath = vsixPath;
                runtimeExists = false;
            }
        }
        return runtimePath;
    }

    /** TD runtime names, used to filter them OUT of non-.td file completions. */
    function getTdNames(): ReadonlySet<string> {
        if (tdNames === undefined) {
            try {
                const content = fs.readFileSync(getRuntimePath(), "utf-8");
                tdNames = extractDeclaredNames(content);
            } catch {
                tdNames = new Set();
            }
        }
        return tdNames;
    }

    function create(info: ts.server.PluginCreateInfo): ts.LanguageService {
        const resolved = getRuntimePath();
        if (!runtimeExists) {
            return info.languageService;
        }

        const host = info.languageServiceHost;

        // Inject td-runtime.d.ts into the project's file list.
        // getExternalFiles does not reliably add files to inferred projects.
        const originalGetScriptFileNames = host.getScriptFileNames.bind(host);
        host.getScriptFileNames = () => {
            const files: string[] = originalGetScriptFileNames();
            if (!files.some((f: string) => isTdFile(f))) return files;
            if (files.includes(resolved)) return files;
            return [...files, resolved];
        };

        // Exclude DOM lib for projects containing .td files to avoid unrelated
        // completions (RemotePlayback, XMLHttpRequestUpload, etc.).
        // Without a tsconfig, TypeScript loads the default lib which includes DOM.
        const originalGetCompilationSettings = host.getCompilationSettings.bind(host);
        host.getCompilationSettings = () => {
            const settings = originalGetCompilationSettings();
            // Use current host method (not stale pre-override reference) so that
            // files added by other plugins or tsserver updates are seen.
            const allFiles: string[] = host.getScriptFileNames();
            if (!allFiles.some((f: string) => isTdFile(f))) {
                return settings;
            }
            return {
                ...settings,
                target: modules.typescript.ScriptTarget.ES2020,
                lib: ["lib.es2020.d.ts"],
            };
        };

        // Filter completions per file type:
        // - .td files: blocklist — hide ES lib names, keep keywords/members/locals by kind
        // - non-.td files: blocklist — remove TD runtime names that leak from shared projects
        const proxy = new Proxy(info.languageService, {
            get(target, prop, receiver) {
                if (prop === "getCompletionsAtPosition") {
                    return (fileName: string, position: number, options: ts.GetCompletionsAtPositionOptions) => {
                        const result: ts.WithMetadata<ts.CompletionInfo> | undefined =
                            target.getCompletionsAtPosition(fileName, position, options);
                        if (result === undefined) return result;

                        if (isTdFile(fileName)) {
                            const filtered = result.entries.filter(
                                e => ALWAYS_ALLOWED_KINDS.has(e.kind) || !ES_LIB_BLOCKLIST.has(e.name)
                            );
                            return { ...result, entries: filtered };
                        }

                        // In non-.td files, filter out TD runtime names
                        const names = getTdNames();
                        if (names.size === 0) return result;
                        const filtered = result.entries.filter(e => !names.has(e.name));
                        return { ...result, entries: filtered };
                    };
                }
                return Reflect.get(target, prop, receiver);
            },
        });

        return proxy;
    }

    return { create };
}

export = init;
